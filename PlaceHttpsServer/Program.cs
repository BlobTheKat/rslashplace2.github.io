app.MapGet("/", () => Results.Content(indexTemplate, "text/html", Encoding.Unicode));

app.MapGet("/place", () =>
{
	var stream = new FileStream(Path.Join(Directory.GetCurrentDirectory(), "place"), FileMode.Open);
	return Results.File(stream);
});

// Lists all available backups. 
app.MapGet("/backups", () =>
{
	var dir = Directory.GetFiles(Directory.GetCurrentDirectory()).ToList();
    for (var fn = 0; fn < dir.Count; fn++)
    {
        dir[fn] = $"<a href=\"{new DirectoryInfo(dir[fn]).Name}\">{new DirectoryInfo(dir[fn]).Name}</a>";
    }

    return Task.FromResult(Results.Content(backuplistTemplate + dir.Aggregate((a, b) => a + "<br>\n" + b), "text/html"));
});

// To download a specific backup.
app.MapGet("/backups/{placeFile}", (string placeFile) =>
{
	var stream = new FileStream(Path.Join(Directory.GetCurrentDirectory(), placeFile), FileMode.Open);
	return Results.File(stream);
});

app.MapGet("/backuplist", async () =>
	await File.ReadAllTextAsync(Path.Join(Directory.GetCurrentDirectory(), "backuplist.txt"))
);

app.MapPost("/timelapse", async (context) =>
{
	var json = JsonSerializer.Deserialize<TimelapseInfo>(context.Request.Body);
	if (json is null) return;
	await new TimelapseGen().GenerateTimelapse(json.BackupStart, json.BackupEnd, json.Fps, json.SX, json.SY, json.EX, json.EY, 500, 750);
});

app.Run();